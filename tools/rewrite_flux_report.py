def rewrite(path: Path) -> None:
    doc = Document(str(path))
    n_paras = len(doc.paragraphs)

    if n_paras <= TITLE_LAST:
        raise SystemExit(f"Слишком мало абзацев: {path}")

    start = TITLE_LAST + 1
    for offset, text in enumerate(NEW_BODY):
        idx = start + offset
        if idx >= n_paras:
            break
        doc.paragraphs[idx].text = text

    # Очистить хвост
    tail_begin = start + len(NEW_BODY)
    for idx in range(tail_begin, n_paras):
        doc.paragraphs[idx].text = ""

    fill_tables(doc)
    doc.save(str(path))


